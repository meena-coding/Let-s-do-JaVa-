public class ForLoop {

    public static void main(String[] args) {

        //For printing numbers from 1 to 100

        //with while loop

        int i = 1;
        while(i <= 100){
            System.out.print(i + " ");
            i++;
        }
        System.out.println();

        //now let's do this with the For Loop

        for(int j = 1; j <= 9999; j++){
            System.out.print(j + " ");
        }
    }
}
